function extractNames(obj) {
  if (typeof obj !== "object" || obj === null) return;

  const result = {};

  if ("name" in obj) {
    result.name = obj.name;
  }

  for (let key in obj) {
    if (key !== "name") {
      const child = obj[key];
      if (Array.isArray(child)) {
        // 处理数组情况
        result[key] = child.map((item) => extractNames(item)).filter(Boolean);
      } else if (typeof child === "object" && child !== null) {
        // 处理嵌套对象情况
        result[key] = extractNames(child);
      }
    }
  }

  return Object.keys(result).length > 0 ? result : undefined; // 如果结果为空，则返回undefined
}

